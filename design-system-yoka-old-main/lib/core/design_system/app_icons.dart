  // ========================================
  // RESPONSIVE ICON METHODS
  // ========================================
  
  /// Responsive icon berdasarkan screen size
  static Icon responsiveIcon({
    required BuildContext context,
    required IconData iconData,
    double? mobileSize,
    double? tabletSize,
    double? desktopSize,
    double? largeDesktopSize,
    Color? color,
  }) {
    final width = MediaQuery.of(context).size.width;
    double size;
    
    if (width >= 1600 && largeDesktopSize != null) {
      size = largeDesktopSize;
    } else if (width >= 1200 && desktopSize != null) {
      size = desktopSize;
    } else if (width >= 900 && tabletSize != null) {
      size = tabletSize;
    } else if (width >= 600 && mobileSize != null) {
      size = mobileSize;
    } else {
      size = mobileSize ?? md;
    }
    
    return Icon(
      iconData,
      size: size,
      color: color,
    );
  }

  // ========================================
  // THEME-BASED ICON METHODS
  // ========================================
  
  /// Icon berdasarkan tema (light/dark)
  static Icon themeIcon({
    required BuildContext context,
    required IconData lightIcon,
    required IconData darkIcon,
    double? size,
    Color? color,
  }) {
    final brightness = Theme.of(context).brightness;
    final iconData = brightness == Brightness.dark ? darkIcon : lightIcon;
    
    return Icon(
      iconData,
      size: size ?? md,
      color: color,
    );
  }

  // ========================================
  // ANIMATED ICON METHODS
  // ========================================
  
  /// Animated icon dengan rotation
  static Widget animatedIcon({
    required IconData iconData,
    required Animation<double> animation,
    double? size,
    Color? color,
  }) {
    return AnimatedBuilder(
      animation: animation,
      builder: (context, child) {
        return Transform.rotate(
          angle: animation.value * 2 * 3.14159,
          child: Icon(
            iconData,
            size: size ?? md,
            color: color,
          ),
        );
      },
    );
  }
  
  /// Animated icon dengan scale
  static Widget scaleIcon({
    required IconData iconData,
    required Animation<double> animation,
    double? size,
    Color? color,
  }) {
    return AnimatedBuilder(
      animation: animation,
      builder: (context, child) {
        return Transform.scale(
          scale: animation.value,
          child: Icon(
            iconData,
            size: size ?? md,
            color: color,
          ),
        );
      },
    );
  }

