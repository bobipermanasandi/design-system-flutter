    color: color,
  );
  
  /// Membuat Icon widget dengan size extra small
  Icon toIconXs({Color? color}) => Icon(
    this,
    size: AppIcons.xs,
    color: color,
  );
  
  /// Membuat Icon widget dengan size small
  Icon toIconSm({Color? color}) => Icon(
    this,
    size: AppIcons.sm,
    color: color,
  );
  
  /// Membuat Icon widget dengan size medium
  Icon toIconMd({Color? color}) => Icon(
    this,
    size: AppIcons.md,
    color: color,
  );
  
  /// Membuat Icon widget dengan size large
  Icon toIconLg({Color? color}) => Icon(
    this,
